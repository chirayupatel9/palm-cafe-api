// Get all menu items (public endpoint for customers, also works for authenticated users)
app.get('/api/menu', async (req, res) => {
  try {
    let cafeId = null;
    
    // Try to get cafeId from authenticated user first (if token provided)
    // Use auth middleware pattern but don't require it (for public access)
    const token = req.header('Authorization')?.replace('Bearer ', '');
    if (token) {
      try {
        const jwt = require('jsonwebtoken');
        const decoded = jwt.verify(token, JWT_SECRET, { clockTolerance: 600 });
        const user = await User.findById(decoded.userId);
        if (user) {
          // Use impersonation cafe from token (this route does not run auth middleware, so read from decoded)
          if (decoded.impersonatedCafeId) {
            cafeId = decoded.impersonatedCafeId;
          } else {
            const userWithCafe = await User.findByIdWithCafe(user.id);
            if (userWithCafe && userWithCafe.cafe_id) {
              cafeId = userWithCafe.cafe_id;
            }
          }
        }
      } catch (error) {
        // Token invalid or expired, continue as unauthenticated user
      }
    }
    
    // If no cafeId from user, get cafe slug from query param or use default
    if (!cafeId) {
      const cafeSlug = req.query.cafeSlug || 'default';
      try {
        const cafe = await Cafe.getBySlug(cafeSlug);
        if (cafe) {
          cafeId = cafe.id;
        }
      } catch (error) {
        // Cafe table might not exist yet or getBySlug failed; fallback below
      }
      // Fallback: use first active cafe when cafe_id column exists (avoids "cafeId is required" error)
      if (!cafeId) {
        const firstCafe = await Cafe.getFirstActive();
        if (firstCafe) {
          cafeId = firstCafe.id;
        }
      }
    }
    
    logger.debug('[GET /api/menu] Fetching menu items', {
      cafeId,
      hasToken: !!token,
      cafeSlug: req.query.cafeSlug
    });
    
    if (!cafeId) {
      return res.status(400).json({
        error: 'Cafe context required. Provide ?cafeSlug= or log in with a cafe account.',
        code: 'CAFE_REQUIRED'
      });
    }
    
    const menuItems = await MenuItem.getAll(cafeId);
    
    logger.debug('[GET /api/menu] Menu items fetched', {
      cafeId,
      itemCount: menuItems.length
    });
    
    res.json(menuItems);
  } catch (error) {
    logger.error('[GET /api/menu] Error fetching menu items:', error);
    res.status(500).json({ error: 'Failed to fetch menu items' });
  }
});

