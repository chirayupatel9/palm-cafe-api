// Export menu to Excel
app.get('/api/menu/export', auth, async (req, res) => {
  try {
    // Get cafe_id from authenticated user (handles impersonation too)
    let cafeId = null;
    if (req.user) {
      // Check if impersonating
      if (req.impersonation && req.impersonation.isImpersonating) {
        cafeId = req.impersonation.cafeId;
      } else if (req.user.cafe_id) {
        cafeId = req.user.cafe_id;
      }
    }
    
    // Super admin can optionally filter by cafe_id via query param
    if (req.user && req.user.role === 'superadmin' && req.query.cafeId) {
      cafeId = parseInt(req.query.cafeId, 10);
    }
    
    // If no cafeId and not superadmin, try to get from default cafe
    if (!cafeId) {
      try {
        const defaultCafe = await Cafe.getBySlug('default');
        if (defaultCafe) {
          cafeId = defaultCafe.id;
        }
      } catch (error) {
        // Cafe might not exist
      }
    }
    
    if (!cafeId) {
      return res.status(400).json({ 
        error: 'Unable to determine cafe. Please ensure you are logged in and belong to a cafe.' 
      });
    }
    
    // Get current menu items for this cafe
    const menuItems = await MenuItem.getAll(cafeId);
    
    if (!menuItems || menuItems.length === 0) {
      return res.status(404).json({ 
        error: 'No menu items found for this cafe. Please add menu items before exporting.' 
      });
    }
    
    // Get all categories for this cafe, then filter to only those used by exported menu items
    const allCategories = await Category.getAll(cafeId);
    const usedCategoryIds = new Set(menuItems.map(item => item.category_id).filter(Boolean));
    const categories = allCategories.filter(cat => usedCategoryIds.has(cat.id));
    
    // Create workbook and worksheet
    const workbook = XLSX.utils.book_new();
    
    // Menu items worksheet - include Image column for ZIP import compatibility
    const menuData = menuItems.map(item => {
      // Extract image filename from image_url if present
      let imageFilename = '';
      if (item.image_url) {
        // Extract filename from path like /images/menu-item-123.jpg
        const imagePath = item.image_url;
        imageFilename = path.basename(imagePath);
      }
      
      return {
        'Category': item.category_name || 'Uncategorized',
        'Item Name': item.name,
        'Description': item.description || '',
        'Price': item.price,
        'Image': imageFilename,
        'Sort Order': item.sort_order || 0
      };
    });
    
    const menuWorksheet = XLSX.utils.json_to_sheet(menuData);
    XLSX.utils.book_append_sheet(workbook, menuWorksheet, 'Menu Items');
    
    // Categories worksheet - only include categories used by exported items
    const categoryData = categories.map(cat => ({
      'Category Name': cat.name,
      'Description': cat.description || '',
      'Sort Order': cat.sort_order || 0
    }));
    
    const categoryWorksheet = XLSX.utils.json_to_sheet(categoryData);
    XLSX.utils.book_append_sheet(workbook, categoryWorksheet, 'Categories');
    
    // Generate buffer
    const buffer = XLSX.write(workbook, { type: 'buffer', bookType: 'xlsx' });
    
    // Set headers for download
    res.setHeader('Content-Type', 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet');
    res.setHeader('Content-Disposition', 'attachment; filename=palm-cafe-menu.xlsx');
    
    res.send(buffer);
  } catch (error) {
    logger.error('Error exporting menu:', error);
    res.status(500).json({ error: 'Failed to export menu: ' + error.message });
  }
});

// Import menu from Excel
app.post('/api/menu/import', auth, upload.single('file'), async (req, res) => {
  try {
    if (!req.file) {
      return res.status(400).json({ error: 'No file uploaded' });
    }

    // Get cafe_id from authenticated user (handles impersonation too)
    let cafeId = null;
    if (req.user) {
      // Check if impersonating
      if (req.impersonation && req.impersonation.isImpersonating) {
        cafeId = req.impersonation.cafeId;
      } else if (req.user.cafe_id) {
        cafeId = req.user.cafe_id;
      }
    }
    
    // Super admin can optionally filter by cafe_id via query param
    if (req.user && req.user.role === 'superadmin' && req.query.cafeId) {
      cafeId = parseInt(req.query.cafeId, 10);
    }
    
    // If no cafeId, try to get from default cafe
    if (!cafeId) {
      try {
        const defaultCafe = await Cafe.getBySlug('default');
        if (defaultCafe) {
          cafeId = defaultCafe.id;
        }
      } catch (error) {
        // Cafe might not exist
      }
    }
    
    if (!cafeId) {
      return res.status(400).json({ 
        error: 'Unable to determine cafe. Please ensure you are logged in and belong to a cafe.' 
      });
    }

    // Read Excel file
    const workbook = XLSX.read(req.file.buffer, { type: 'buffer' });
    const menuSheet = workbook.Sheets['Menu Items'];
    
    if (!menuSheet) {
      return res.status(400).json({ error: 'Menu Items sheet not found in Excel file' });
    }

    // Convert to JSON
    const menuData = XLSX.utils.sheet_to_json(menuSheet);
    
    if (menuData.length === 0) {
      return res.status(400).json({ error: 'No data found in Menu Items sheet' });
    }

    // Get existing categories for mapping (scoped to this cafe)
    const categories = await Category.getAll(cafeId);
    const categoryMap = {};
    categories.forEach(cat => {
      categoryMap[cat.name.toLowerCase()] = cat.id;
    });

    // Process menu items
    const itemsToImport = [];
    const errors = [];

    for (let i = 0; i < menuData.length; i++) {
      const row = menuData[i];
      const rowNumber = i + 2; // Excel rows start at 1, but we have header

      try {
        // Validate required fields
        if (!row['Item Name'] || !row['Price']) {
          errors.push(`Row ${rowNumber}: Item Name and Price are required`);
          continue;
        }

        // Find or create category (scoped to this cafe)
        let categoryId = null;
        const categoryName = (row['Category'] || row['category'] || row['CATEGORY'] || '').toString().trim();
        if (categoryName) {
          categoryId = categoryMap[categoryName.toLowerCase()];
          
          if (!categoryId) {
            // Create new category (scoped to this cafe)
            try {
              const newCategory = {
                name: categoryName,
                description: '',
                sort_order: categories.length + 1,
                cafe_id: cafeId
              };
              
              const createdCategory = await Category.create(newCategory);
              categoryId = createdCategory.id;
              categoryMap[categoryName.toLowerCase()] = categoryId;
              categories.push(createdCategory);
              logger.info('[MENU IMPORT] Created new category', { categoryId, name: categoryName, cafeId });
            } catch (catError) {
              // If duplicate entry error, try to find existing category (including inactive)
              if (catError.message.includes('Duplicate entry') || catError.message.includes('UNIQUE constraint')) {
                try {
                  // Query database directly to find existing category (including inactive)
                  const [existing] = await pool.execute(
                    'SELECT id, name, is_active FROM categories WHERE name = ? AND cafe_id = ? LIMIT 1',
                    [categoryName, cafeId]
                  );
                  
                  if (existing.length > 0) {
                    categoryId = existing[0].id;
                    categoryMap[categoryName.toLowerCase()] = categoryId;
                    
                    // Reactivate if inactive
                    if (!existing[0].is_active) {
                      await pool.execute(
                        'UPDATE categories SET is_active = TRUE WHERE id = ?',
                        [categoryId]
                      );
                      logger.info('[MENU IMPORT] Reactivated existing category', { categoryId, name: categoryName, cafeId });
                    } else {
                      logger.info('[MENU IMPORT] Found existing category', { categoryId, name: categoryName, cafeId });
                    }
                  } else {
                    // Category exists but belongs to different cafe - this shouldn't happen with UNIQUE constraint
                    const errorMsg = `Row ${rowNumber}: Category "${categoryName}" already exists but couldn't be found for this cafe`;
                    errors.push(errorMsg);
                    if (i < 5) {
                      logger.error('[MENU IMPORT]', errorMsg);
                    }
                    continue;
                  }
                } catch (findError) {
                  const errorMsg = `Row ${rowNumber}: Failed to find existing category "${categoryName}": ${findError.message}`;
                  errors.push(errorMsg);
                  if (i < 5) {
                    logger.error('[MENU IMPORT]', errorMsg, { findError });
                  }
                  continue;
                }
              } else {
                const errorMsg = `Row ${rowNumber}: Failed to create category "${categoryName}": ${catError.message}`;
                errors.push(errorMsg);
                if (i < 5) {
                  logger.error('[MENU IMPORT]', errorMsg, { catError });
                }
                continue; // Skip this item if category creation fails
              }
            }
          }
        } else {
          const errorMsg = `Row ${rowNumber}: Category is required. Please provide a category name. Found columns: ${Object.keys(row).join(', ')}`;
          errors.push(errorMsg);
          if (i < 5) {
            logger.error('[MENU IMPORT]', errorMsg, { row });
          }
          continue;
        }

        // Create menu item
        const priceNum = parseFloat(price);
        if (isNaN(priceNum) || priceNum <= 0) {
          const errorMsg = `Row ${rowNumber}: Invalid price "${price}". Price must be a positive number.`;
          errors.push(errorMsg);
          if (i < 5) {
            logger.error('[MENU IMPORT]', errorMsg);
          }
          continue;
        }
        
        // Handle image URL from Excel
        let imageUrl = null;
        const imageValue = row['Image'] || row['image'] || row['IMAGE'] || '';
        if (imageValue) {
          const imageValueStr = imageValue.toString().trim();
          // If it's a URL (starts with http:// or https://), use it directly
          if (imageValueStr.startsWith('http://') || imageValueStr.startsWith('https://')) {
            imageUrl = imageValueStr;
          } else if (imageValueStr.startsWith('/')) {
            // If it's a path starting with /, use it as is (e.g., /images/item.jpg)
            imageUrl = imageValueStr;
          } else if (imageValueStr) {
            // If it's just a filename, assume it's in /images/ directory
            imageUrl = `/images/${imageValueStr}`;
          }
        }
        
        const menuItem = {
          category_id: categoryId,
          name: itemName.toString().trim(),
          description: (row['Description'] || row['description'] || row['DESCRIPTION'] || '').toString().trim(),
          price: priceNum,
          sort_order: row['Sort Order'] || row['sort_order'] || row['Sort Order'] ? parseInt(row['Sort Order'] || row['sort_order'] || row['Sort Order'] || 0) : 0,
          image_url: imageUrl,
          cafe_id: cafeId
        };

        itemsToImport.push(menuItem);
      } catch (error) {
        const errorMsg = `Row ${rowNumber}: ${error.message}`;
        errors.push(errorMsg);
        if (i < 5) {
          logger.error('[MENU IMPORT] Row processing error', { rowNumber, error: error.message, stack: error.stack, row });
        }
      }
    }
    
    logger.info('[MENU IMPORT] Processing complete', {
      totalRows: menuData.length,
      itemsToImport: itemsToImport.length,
      errors: errors.length,
      firstFewErrors: errors.slice(0, 10)
    });

    // Import items
    logger.info('[MENU IMPORT] Importing items', {
      cafeId,
      itemCount: itemsToImport.length,
      sampleItem: itemsToImport[0] ? {
        name: itemsToImport[0].name,
        category_id: itemsToImport[0].category_id,
        cafe_id: itemsToImport[0].cafe_id
      } : null
    });
    
    const importResults = await MenuItem.bulkImport(itemsToImport);
    
    const successCount = importResults.filter(r => r.success).length;
    const failureCount = importResults.filter(r => !r.success).length;
    
    const importErrors = importResults.filter(r => !r.success);
    if (importErrors.length > 0) {
      logger.error('[MENU IMPORT] Import errors:', importErrors);
    }

    logger.info('[MENU IMPORT] Import completed', {
      cafeId,
      successCount,
      failureCount,
      totalErrors: errors.length + importErrors.length,
      itemsProcessed: itemsToImport.length
    });
    
    // Verify items were actually created by fetching them
    if (successCount > 0) {
      try {
        const createdItems = await MenuItem.getAll(cafeId);
        logger.info('[MENU IMPORT] Verification - Total items in database for cafe', {
          cafeId,
          totalItems: createdItems.length
        });
      } catch (verifyError) {
        logger.error('[MENU IMPORT] Error verifying imported items:', verifyError);
      }
    }

    const allErrors = errors.concat(importResults.filter(r => !r.success).map(r => r.error));
    
    res.json({
      message: `Import completed. ${successCount} items imported successfully, ${failureCount} failed.`,
      successCount,
      failureCount,
      errors: allErrors.length > 0 ? allErrors : undefined,
      cafeId: cafeId // Include cafeId in response for debugging
    });

  } catch (error) {
    logger.error('[MENU IMPORT] Error importing menu:', error);
    logger.error('[MENU IMPORT] Error stack:', error.stack);
    res.status(500).json({ 
      error: 'Failed to import menu',
      details: error.message,
      stack: process.env.NODE_ENV === 'development' ? error.stack : undefined
    });
  }
});

